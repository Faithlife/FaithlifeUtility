namespace Faithlife.Utility
{
	/// <summary>
	/// Indicates whether an operation should be sensitive to case.
	/// </summary>
	public enum CaseSensitivity
	{
		/// <summary>
		/// The operation should be sensitive to case.
		/// </summary>
		MatchCase,

		/// <summary>
		/// The operation should be insensitive to case.
		/// </summary>
		IgnoreCase,
	}
}
